package Accessmodifier;

public class Students {
	  int id;  
	    String name;  
	    //creating a parameterized constructor  
	    Students (int i,String n){  
	    id = i;  
	    name = n;  
	    }  
	    //method to display the values  
	    void display()
	    {
	    	System.out.println(id+" "+name);
	    	}  
}
